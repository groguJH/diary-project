export function getRandomDiaryPrompt(currentPrompt?: string) {
  if (diaryPrompts.length === 1) {
    return diaryPrompts[0];
  }

  let nextPrompt = diaryPrompts[Math.floor(Math.random() * diaryPrompts.length)];

  while (nextPrompt === currentPrompt) {
    nextPrompt = diaryPrompts[Math.floor(Math.random() * diaryPrompts.length)];
  }

  return nextPrompt;
}
